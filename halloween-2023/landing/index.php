            <h2 class="sec__article__title sec--third__article__title">Qué es todo esto?</h2>

            <p class="sec__article__text sec--third__article__text">
                Cuando estamos aprendiendo programación, muchas veces nos encontramos perdidos en el abismo de los tutoriales, viendo videos y videos sin parar, pero sin hacer ni un solo proyecto.
            </p>
            <p class="sec__article__text sec--third__article__text">
                Al terminar de ver los videos creemos que entendemos todo pero si nos ponemos manos a la obra nos cuesta muchísimo arrancar o solucionar los problemas que nos van surgiendo.
            </p>
            <p class="sec__article__text sec--third__article__text">
                La mejor manera de superar eso, y también de ganar la confianza necesaria para dejar atrás el síndrome del impostor, es practicar, practicar y practicar.
            </p>
            <p class="sec__article__text sec--third__article__text">
                <em class="sec__article__text__em sec--third__article__text__em">
                Y eso es esto, proyectos prácticos para que aprendas programación web de una forma entretenida.
                </em>
            </p>
            <p class="sec__article__text sec--third__article__text">
                Los proyectos irán desbloqueándose en las fechas indicadas, acompañados del código en mi Github y pequeños tutoriales en mis redes sociales.
            </p>
            <p class="sec__article__text sec--third__article__text">
                Así aque anímate a ver los proyectos que están aquí recogidos y a replicarlos por tu cuenta,
            </p>
            <p class="sec__article__text sec--third__article__text">
                yo estaré encantada de que me envíes cualquiera de ellos! Si tengo tiempo puedo intentar resolver tus dudas o darte consejos sobre el código.
            </p>

